package Loops;

public class ForEachArray {
	public static void main(String[] args) {

		int a[] = {10,20,30,40,50};
		
		for(int i:a)
		{
			System.out.println(i);
		}
		
		System.out.println();
		
		char ch[] = {'j','a','v','a'};
		
		for(char ch1:ch)
		{
			System.out.print(ch1);
		}
		
		
		
		
		
		
	}

}
